        <h2><strong>Acerca de Nosotros</strong></h2>
        <p>El último domingo de cada mes, una iniciativa de peluqueros, estilistas y profesionales del cuidado del
          cabello
          <strong>DE TODO EL PAIS </strong>que decidieron llevar adelante una acción concreta para acompañar y brindar
          sus servicios a los sectores más desprotegidos de la sociedad.
          El objetivo es brindar un servicio de peluquería gratuito a distintas zonas con Dificultades sociales y
          económicas de las principales ciudades del país: comedores, merenderos, asociaciones barriales,
          entre otros lugares. Esta iniciativa se realiza el último domingo de cada mes, en las provincias de
          Jujuy, Chaco, Misiones, Corrientes, Formosa, Catamarca, La Rioja, Santa Fe, Tucumán, Santiago del Estero,
          Salta, Buenos Aires, Cordoba, Rosario, San Juan, Rio Negro y localidades como Peguajo, Rafaela, Tartagal, Mar
          del Plata. Actualmente son más de (1.000) profesionales que participan de Tijeras Solidarias.
        </p>
        <spam id="readMore">
          <p>En todo el país; constituyendo de esta manera una <strong>FUNDACIÓN NACIONAL</strong>. La entidad carece
            de fines de lucro y tendrá por objeto mejorar el nivel de vida de la Comunidad en general, especialmente
            quellas personas que por su condición de pobreza y de escasos recursos se encuentren en un estado de
            vulnerabilidad y marginalidad. Para ello, se llevará a cabo las siguientes actividades:
            Sostener un comedor comunitario, para brindarle almuerzo, merienda y de esta manera poder atender
            necesidades básicas en materia de alimentación. Impulsar la creación de una huerta con el fin de
            fomentar la cultura del trabajo y además para que sea fuente de producción de insumos para el comedor
            <strong>PROMOVER</strong> la creación de talleres de capacitación (formal o no formal) y el dictado de
            cursos y seminarios con el fin de formar a la persona para que esta cuente con una herramienta eficaz
            e insertarse en el mercado laboral y tenga la posibilidad de proyectar diferentes micro
            emprendimientos, las actividades detalladas no son limitativas, pudiendo realizar otras
            no previstas pero que tengan relación directa con su objeto SOBRE TIJERAS SOLIDARIAS.
            Atender a niños y niñas que no tienen la posibilidad de pagar una atención en un salon,
            el puntapié y la excusa para realizar una actividad solidaria concreta , que va más allá de un simple corte
            de pelo, se trata de escuchar,
            de mirar a los ojos y de mimar a los niños. Además del servicio de peluquería, tijeras solidarias recolecta
            distintos tipos de donaciones (ropa , alimentos,
            pañales, leche larga vida, etc ) que son entregados a los lugares donde se realiza la acción. Los salones
            que participan se constituyen en un punto de recolección
            de las donaciones recibidas. Desde Tijeras Solidarias creemos firmemente que pequeños actos son los que van
            a cambiar el mundo, y desde ese concepto, invitamos a toda
            la sociedad a sumarse a este proyecto y colaborar de la manera que puedan, para poder seguir haciendo esto
            posible. </p>

            <p><strong>“La educación es una herramienta que podemos usar para el futuro.</strong> Ya que nuestra
              fundación realiza hincapié en que cada persona pueda tener los conocimientos para
              crecer y llevar una vida amena y culturizada”
              Desde tijeras solidarias queremos garantizar el acceso a la educación de los adolecentes
              de ultimo año de la secundaria es un componente esencial de nuestro mandato en
              materia de protección y búsqueda de soluciones duraderas. Mediante el acceso a la
              educación para su crecimiento personal y autosustentable se logra que reciban los
              conocimientos que les permitirán labrarse un futuro. También, se contribuye a que éstos
              encuentren en las aulas un espacio seguro donde puedan conectarse consigo mismos<br>
              
              Programa Educativo: Un futuro para todos.<br>
              
              Nuestro programa está destinado específicamente para escuelas rurales para adolecentes
              del último año de la secundaria.
              El mismo tiene como fin la enseñanza del rubro peluquería. Dicho programa está pensado
              para desarrollarse durante un año y medio. La primera etapa o primer año será 10 meses
              estimativos desde marzo a diciembre con un modulo semanal completando ocho
              unidades.
              El segundo año o segunda etapa será de especialización y tendrá un periodo de 5 meses
              estimativos con un modulo semanal como en el año anterior
              Los módulos de acuerdo a la materia serán uno de 90’ o dos módulos de 60’ con un
              recreo de 10’ entre medio.
              Material brindado por la fundación:
              - Fotocopias a color
              - Cuadernillos
              - Soporte técnico
              - Evaluaciones multiple choice
              - Herramientas básicas para el desarrollo del programa
              - Refrigerio para los intervalos
              También contamos con un programa de reconocimiento:
              Al alumno que mas destaque al tercer mes desde el inicio del programa será premiado
              durante los 7 meses restantes y de acuerdo a su desempeño anual continuara su
              reconocimiento durante el siguiente año para su práctica con material especial para su
              graduación.<br>
              
              
              Biblioteca: Libros destacados de interés cultural. Con el objetivo de desarrollar el interés
              por la lectura/ escritura
              Todo el programa educativo se da acompañado de psicopedagogía y tutores
              especialmente capacitados para cumplir las funciones de seguimiento y orientación
              con el fin de personalizar en cada alumno su desempeño y así poder incentivarlos<br>
              
              Misión: Es brindar igualdad de oportunidades a la hora de estudiar, acercando una
              propuesta que permita al individuo su realización como persona y posibilidades de un
              futuro próspero, basado en los pilares de la formación, el compromiso y la calidad.
              Visión: Nuestra visión es ser referentes en educación para adolecentes del último año de
              secundario, con un alto compromiso social sustentado en la calidad educativa y seriedad
              de la misma.<br>
              <strong>Valores: Seriedad, Responsabilidad, Compromiso social y Liderazgo.</strong></p>

